    public void mostrarPedidoExtendido(int nOrden){
        PedidosExtendido rPedidoExt = rRed.MostrarProductos(nOrden);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{Integer.toString(rPedidoExt.getCanitdad()), jComboBox1.getItemAt(rPedidoExt.getProducto()), rPedidoExt.getPrecio(), rPedidoExt.getPrecio()*rPedidoExt.getCanitdad()});
        
    }
    
    public void labelTotal(int nOrdenr){
        PedidosExtendido rPedidoExt = rRed.MostrarProductos(nOrdenr);
        jLabel11.setText(String.valueOf(rPedidoExt.getPrecio()*rPedidoExt.getCanitdad()));
    }
