    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //Aqui se añade una fila a la tabla hay que hacer una funcion para obtener el precio de un producto
        //Esta función se usa en las dos ultimas posiciones de la tabla (Precio y subtotal = cantidad * precio)
        model.addRow(new Object[]{Math.abs(Integer.parseInt(jTextField3.getText())),jComboBox1.getSelectedItem(),rRed.getPrecio(jComboBox1.getSelectedItem().toString()), Math.abs(Double.parseDouble(jTextField3.getText()))*rRed.getPrecio(jComboBox1.getSelectedItem().toString())});
        //Calcula el total 
        double subtotal;
        for (int x=0;x<model.getRowCount();x++){
            subtotal =  (double) model.getValueAt(x, 3);
            total = total + subtotal;
            jLabel11.setText(String.valueOf(total));
        }
        Producto p = new Producto(rRed.getCode(jComboBox1.getSelectedItem().toString()),jComboBox1.getSelectedItem().toString(),rRed.getPrecio(jComboBox1.getSelectedItem().toString()));
        rRed.AgregarProducto(contador, Integer.parseInt(jTextField3.getText()), p);
        
    }//GEN-LAST:event_jButton3ActionPerformed

